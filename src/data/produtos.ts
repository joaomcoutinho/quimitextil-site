export function produtosPorSegmento(slug: string): Produto[] {
  return produtos.filter(p => p.segmentos.includes(slug as SegmentoSlug))
}

export function produtosRelacionados(slug: string, limit = 4): Produto[] {
  const produto = produtos.find(p => p.slug === slug)
  if (!produto) return []
  return produtos
    .filter(p => p.slug !== slug && (
      p.categoria === produto.categoria ||
      p.segmentos.some(s => produto.segmentos.includes(s))
    ))
    .slice(0, limit)
}
